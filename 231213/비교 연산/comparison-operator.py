# 두 정수 a, b 입력 받기
a, b = map(int, input().split())

# a가 b보다 같거나 큰가?
if a >= b:
    print(1)
else:
    print(0)

# a가 b보다 큰가?
if a > b:
    print(1)
else:
    print(0)

# b가 a보다 같거나 큰가?
if b >= a:
    print(1)
else:
    print(0)

# b가 a보다 큰가?
if b > a:
    print(1)
else:
    print(0)

# a와 b가 같은가?
if a == b:
    print(1)
else:
    print(0)

# a와 b가 다른가?
if a != b:
    print(1)
else:
    print(0)